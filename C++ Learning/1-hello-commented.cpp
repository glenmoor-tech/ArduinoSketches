// This is the input output stream library
// It is used to read and write input to
// the console.
#include <iostream>
// This tells the program to use the standard (std)
// namespace.
using namespace std;

// This is the main function. It is where all
// C++ programs begin their execution.
int main() {
    // This line calls the method cout or console output.
    // This writes a string to the console.
    // The \n is a escape sequence for a new line
    // in the console.
    cout << "Hello World!\n";
}